package loveProgramming;

public class Four {
    public int getCountDown() {

        //While loop

        int countDown = 10;
        //System.out.println("CountDown starts from"+countDown);
        while(countDown>0){
            countDown--;
//            System.out.println("Countdown: " + countDown--);
            ;
        }
        return countDown;
    }



}
